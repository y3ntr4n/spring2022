package com.java.class11;
//Write a program to print 10 to 1
public class HomeWork2 {
    public static void main(String[] args) {
        int i = 10;
        while (i >= 0){
            System.out.println(i);
            i--;
        }

        System.out.println();

        int j = 1;
        while (j <= 10) {
            System.out.println(11-j);
            j++;
        }

        System.out.println();

        int k = 1;
        int l = 10;
        while (k <= 10) {
            System.out.println(l);
            k++;
            l--;
        }
    }
}
