package com.java.class04;

public class MathematicalOperators {

    public static void main(String[] args) {

        /* Strings-
        Non primitive data types
        Stores text
        must be in double quotes ""
        */

        String myFavBooks = "The Power Of Now";
        System.out.println(myFavBooks);

        String hello = "hello";
        System.out.println(hello);
        String w = "world";
        System.out.println(w);

        System.out.println("hello + w");
        //everything in between double quotes are part of the text (String)

        System.out.println(hello+w);
        //+ between two variables is CONCATENATION

        String firstName =  "Jane ";//put space after
        String lastName = "Doe ";//put space after
        String dob = "02/22/2022";

        System.out.println();//give one empty space between lines

        System.out.println(firstName + lastName + dob);











    }

}
