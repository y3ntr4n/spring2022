        System.out.println("" + (a + b));
        // 21

        //precedence - order of operations in java
        // ++, --, unary -, *, /, %, assignment =
        //when you have 2 diff data types in an operation, java will run results using the biggest data type

        //assignment operators
        // =, +=, -=, *=, /+, %=
        // num2 = num1 ->
        // num2 += num1 -> num2 = num2 + num1
        // num2 -+ num1 -> num2 = num2 - num1
        // num2 *= num1 -> num2 * num1
        // num2 /+ num1 -> num2 /  num1
        // num2 % num1 -> num2 % num1



