package com.java.class03;

public class ConsolePrinting {
    public static void main(String[] args) {
        System.out.println("My Favorite Three Books:");
        //println() - prints new line

        System.out.print("The Power of Now, Seven Habits of Highly Successful People, 10x Rule");
        //print() same line
        //print new line \n

        System.out.println("The Power of Now, \nSeven Habits of Highly Successful People, \n10x Rule");
        //print special character, precede with \

        System.out.println("Item\t$20");
        //print tabs \t

        System.out.println("Item\t\t$20");
        //print double tabs \t\t

        System.out.println("Item\n$20");
        //print new line \n

        System.err.println("This is an error created by me");
        System.err.print("Second error message");

//multiple lines comments
        /*hello
        my
        name
        is
        Yen
         */


    }
}
